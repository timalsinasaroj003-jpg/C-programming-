/*program to demonstrate the use of different data types in C use of print only*/

#include <stdio.h>
int main()
{
    char c = 'A';
    int i = 42;
    float f = 3.14159;
    

    printf("Character: %c\n", c);
    printf("Integer: %d\n", i);
    printf("Float: %f\n", f);
    
    return 0;
}