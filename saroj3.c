/* program to declare and initialize variables OF DIFFERENT TYPES*/


#include<stdio.h>
int main () {
    int age = 25;
    char grade = 'B';
    float height = 5.8;
    
    printf("Age: %d\n", age);
    printf("Grade: %c\n", grade);
    printf("Height: %f\n", height);
    

    return 0;
}